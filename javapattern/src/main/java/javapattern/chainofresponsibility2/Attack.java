package javapattern.chainofresponsibility2;

public class Attack {

	private int amount;

	/**
	 * @param amount
	 */
	public Attack(int amount) {
		super();
		this.amount = amount;
	}

	public int getAmount() {
		return amount;
	}

	public void setAmount(int amount) {
		this.amount = amount;
	}
	
	
}
